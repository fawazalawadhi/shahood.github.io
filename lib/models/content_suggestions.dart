import 'event_category.dart';

/// اقتراحات جاهزة تساعد المستخدم يبدأ بسرعة، تختلف حسب نوع المناسبة.
/// تُعرض كـ "شرائح اقتراح" (Chips) في شاشة التصميم، وبمجرد الضغط عليها
/// تُملأ في الحقل المناسب مباشرة.
class ContentSuggestions {
  ContentSuggestions._();

  static List<String> welcomeNotesFor(EventCategory category) {
    switch (category) {
      case EventCategory.wedding:
        return [
          'ويجعل لكم من أنفسكم أزواجاً... يتشرف الأهل بدعوتكم لحضور حفل زفاف',
          'قال تعالى: ومن آياته أن خلق لكم من أنفسكم أزواجاً لتسكنوا إليها',
          'بكل الحب والفرح ندعوكم لمشاركتنا أجمل لحظات حياتنا',
        ];
      case EventCategory.engagement:
        return [
          'يسعدنا ويشرفنا دعوتكم لحضور حفل خطوبتنا',
          'بقلوب مليئة بالفرح ندعوكم لمشاركتنا بداية قصتنا',
        ];
      case EventCategory.newborn:
        return [
          'اللهم بارك لنا في مولودنا واجعله من الصالحين',
          'بقلوب مفعمة بالفرح والامتنان نزف لكم بشرى مولودنا الجديد',
          'هدية من الله وسعادة لا توصف، اهلاً بمولودنا الجديد',
        ];
      case EventCategory.graduation:
        return [
          'بعد جهد ومثابرة، نفخر بدعوتكم لمشاركتنا فرحة التخرج',
          'وقل ربِّ زدني علماً - يسعدنا دعوتكم لحفل تخرجنا',
        ];
      case EventCategory.general:
        return [
          'يسعدنا ويشرفنا دعوتكم لمشاركتنا هذه المناسبة السعيدة',
        ];
    }
  }

  /// أفكار عامة لكل تصنيف تساعد المستخدم يعرف وش يحط بالحقول الثانية
  static List<String> tipsFor(EventCategory category) {
    switch (category) {
      case EventCategory.wedding:
        return [
          'أضف اسم العريس والعروسة في "أسماء أصحاب المناسبة"',
          'فعّل قسم "الهدايا" لو تبي تستقبل هدايا نقدية',
          'استخدم عداد تنازلي لحماس أكثر قبل يوم الزفاف',
        ];
      case EventCategory.engagement:
        return [
          'أضف اسم الخاطب والمخطوبة كل واحد بسطر',
          'معرض الصور مناسب لصور الخطوبة أو صور تجمع الطرفين',
        ];
      case EventCategory.newborn:
        return [
          'اكتب اسم المولود ووزنه في نص الأسماء أو الشعر الترحيبي',
          'معرض الصور مثالي لأول صور المولود',
          'عطّل قسم "الهدايا" لو ما تبي تستقبل هدايا مالية',
        ];
      case EventCategory.graduation:
        return [
          'اذكر التخصص والجامعة في نص الترحيب',
          'معرض الصور مناسب لصور حفل التخرج أو الشهادة',
        ];
      case EventCategory.general:
        return ['خصّص كل قسم حسب طبيعة مناسبتك'];
    }
  }
}
